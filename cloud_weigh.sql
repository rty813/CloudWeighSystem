DROP TABLE IF EXISTS `cloud_weigh`;
CREATE TABLE `cloud_weigh` (
  ID integer NOT NULL AUTO_INCREMENT,
  DEVICE_ID integer NOT NULL,
  VERSION varchar(20) NOT NULL,
  STATUS integer NOT NULL DEFAULT 0, 
  PM2_5 float NOT NULL,
  PM10 float NOT NULL,
  TSP float,
  NOISE float NOT NULL,
  TEMP float NOT NULL,
  HUMI float NOT NULL,
  AIR_SPEED float NOT NULL,
  AIR_DIRECTION float NOT NULL, 
  PRESSURE float,
  OTHER1 VARCHAR(255),
  OTHER2 VARCHAR(255),
  OTHER3 VARCHAR(255),
  OTHER4 VARCHAR(255),
  OTHER5 VARCHAR(255),
  LATITUDE VARCHAR(255),
  LONGITUDE VARCHAR(255),
  TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (ID)
) DEFAULT CHARSET=utf8;
